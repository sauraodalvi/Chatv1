// Function to get a random selection of characters
export const getRandomCharacters = (count = 3) => {
  const shuffled = [...characterLibrary].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Function to find a character by name
export const findCharacterByName = (name) => {
  return characterLibrary.find(char => char.name === name);
};
